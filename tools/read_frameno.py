#!/usr/bin/env python3
"""実機/エミュ録画のデバッグオーバーレイ先頭 "F<4桁16進>" から movie フレーム番号を読む。

デバッグ欄(dbg=1)の1行目は "FXXXX RXXXX SXXXX ..." で、先頭 F の直後4桁が現在の
movie フレーム番号(16進, boot/dbgfont.bin の 8x8 ビットマップフォント)。8x8セルを
テンプレート(gen_debugfont.py と同じ字形)と正規化相互相関(NCC)で照合して読む。
背景映像に強いよう NCC(明暗オフセット不変) + y自動較正 + 呼び出し側で単調性補正。

使い方(ライブラリ): from read_frameno import read_frameno; n = read_frameno(pil_img_256x192)
"""
import numpy as np

# gen_debugfont.py と同じ 0-F 字形("#"=点灯)
_HEX = {
    0x0: ["..####..", ".##..##.", ".##..##.", ".##..##.", ".##..##.", ".##..##.", "..####..", "........"],
    0x1: ["...##...", "..###...", "...##...", "...##...", "...##...", "...##...", ".######.", "........"],
    0x2: [".#####..", "##...##.", "....##..", "...##...", "..##....", ".##.....", "#######.", "........"],
    0x3: ["#####...", "....##..", "...###..", "....##..", "....##..", "##..##..", ".####...", "........"],
    0x4: ["...###..", "..####..", ".##.##..", "##..##..", "#######.", "....##..", "....##..", "........"],
    0x5: ["#######.", "##......", "######..", ".....##.", ".....##.", "##...##.", ".#####..", "........"],
    0x6: ["..####..", ".##.....", "##......", "######..", "##...##.", "##...##.", ".#####..", "........"],
    0x7: ["#######.", "....##..", "...##...", "..##....", "..##....", "..##....", "..##....", "........"],
    0x8: [".#####..", "##...##.", "##...##.", ".#####..", "##...##.", "##...##.", ".#####..", "........"],
    0x9: [".#####..", "##...##.", "##...##.", ".######.", ".....##.", "....##..", ".####...", "........"],
    0xA: ["..###...", ".##.##..", "##...##.", "##...##.", "#######.", "##...##.", "##...##.", "........"],
    0xB: ["######..", "##...##.", "##...##.", "######..", "##...##.", "##...##.", "######..", "........"],
    0xC: ["..####..", ".##..##.", "##......", "##......", "##......", ".##..##.", "..####..", "........"],
    0xD: ["#####...", "##..##..", "##...##.", "##...##.", "##...##.", "##..##..", "#####...", "........"],
    0xE: ["#######.", "##......", "##......", "#####...", "##......", "##......", "#######.", "........"],
    0xF: ["#######.", "##......", "##......", "#####...", "##......", "##......", "##......", "........"],
}
_T = {v: np.array([[1.0 if c == "#" else 0.0 for c in r] for r in rows]) for v, rows in _HEX.items()}

X_F = 8                      # "F" の左端x(実測)
DIGX = [16, 24, 32, 40]      # 直後4桁のx
_Fg = _T[0xF]


def _ncc(a, b):
    a = a - a.mean(); b = b - b.mean()
    d = np.sqrt((a * a).sum() * (b * b).sum())
    return float((a * b).sum() / d) if d > 1e-6 else -1.0


def _calib_y(gray):
    best, by = -2.0, 10
    for y in range(4, 16):
        s = _ncc(gray[y:y + 8, X_F:X_F + 8].astype(float), _Fg)
        if s > best:
            best, by = s, y
    return by, best


def read_frameno(img):
    """PIL Image(256x192) または grayscale ndarray -> (frame_no, confidence)。読めなければ (-1, conf)。"""
    if hasattr(img, "convert"):
        gray = np.asarray(img.convert("L"))
    else:
        gray = np.asarray(img)
        if gray.ndim == 3:
            gray = gray.mean(axis=2)
    y, fconf = _calib_y(gray)
    val = 0
    minsc = 2.0
    for x in DIGX:
        cell = gray[y:y + 8, x:x + 8].astype(float)
        best, bv = -2.0, 0
        for v, t in _T.items():
            s = _ncc(cell, t)
            if s > best:
                best, bv = s, v
        val = val * 16 + bv
        minsc = min(minsc, best)
    conf = min(fconf, minsc)
    return val, conf


if __name__ == "__main__":
    import sys
    from PIL import Image
    for p in sys.argv[1:]:
        n, c = read_frameno(Image.open(p))
        print("%s -> frame %d (0x%04X) conf=%.3f" % (p, n, n, c))
