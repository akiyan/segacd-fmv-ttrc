#!/usr/bin/env python3
"""Generate a 16-glyph hex font (0-F) as Mega Drive 4bpp tiles.

Output: boot/hexfont.bin = 16 tiles * 32 bytes = 512 bytes.
Glyph N (0..15) is tile N; set pixels use colour index 1, background 0.
The Main CPU bench ROM uploads these at VRAM tile index 1 (so glyph for
nibble v lives at tile 1+v) and writes name-table entries to print numbers.
"""
from pathlib import Path

# 8x8 bitmaps, '#' = colour 1. Bold 5-7px strokes so they survive emulator
# downscaling in headless screenshots.
GLYPHS = {
    0x0: ["..####..", ".##..##.", ".##..##.", ".##..##.", ".##..##.", ".##..##.", "..####..", "........"],
    0x1: ["...##...", "..###...", "...##...", "...##...", "...##...", "...##...", ".######.", "........"],
    0x2: [".#####..", "##...##.", "....##..", "...##...", "..##....", ".##.....", "#######.", "........"],
    0x3: ["#####...", "....##..", "...###..", "....##..", "....##..", "##..##..", ".####...", "........"],
    0x4: ["...###..", "..####..", ".##.##..", "##..##..", "#######.", "....##..", "....##..", "........"],
    0x5: ["#######.", "##......", "######..", ".....##.", ".....##.", "##...##.", ".#####..", "........"],
    0x6: ["..####..", ".##.....", "##......", "######..", "##...##.", "##...##.", ".#####..", "........"],
    0x7: ["#######.", "....##..", "...##...", "..##....", "..##....", "..##....", "..##....", "........"],
    0x8: [".#####..", "##...##.", "##...##.", ".#####..", "##...##.", "##...##.", ".#####..", "........"],
    0x9: [".#####..", "##...##.", "##...##.", ".######.", ".....##.", "....##..", ".####...", "........"],
    0xA: ["..###...", ".##.##..", "##...##.", "##...##.", "#######.", "##...##.", "##...##.", "........"],
    0xB: ["######..", "##...##.", "##...##.", "######..", "##...##.", "##...##.", "######..", "........"],
    0xC: ["..####..", ".##..##.", "##......", "##......", "##......", ".##..##.", "..####..", "........"],
    0xD: ["#####...", "##..##..", "##...##.", "##...##.", "##...##.", "##..##..", "#####...", "........"],
    0xE: ["#######.", "##......", "##......", "#####...", "##......", "##......", "#######.", "........"],
    0xF: ["#######.", "##......", "##......", "#####...", "##......", "##......", "##......", "........"],
}


def tile_bytes(rows):
    out = bytearray()
    for r in rows:
        r = (r + "........")[:8]
        for c in range(0, 8, 2):
            hi = 1 if r[c] == "#" else 0
            lo = 1 if r[c + 1] == "#" else 0
            out.append((hi << 4) | lo)
    assert len(out) == 32
    return out


def main():
    data = bytearray()
    for v in range(16):
        data += tile_bytes(GLYPHS[v])
    out = Path(__file__).resolve().parent.parent / "boot" / "hexfont.bin"
    out.write_bytes(data)
    print(f"wrote {out} ({len(data)} bytes)")


if __name__ == "__main__":
    main()
