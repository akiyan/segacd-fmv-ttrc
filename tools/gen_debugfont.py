#!/usr/bin/env python3
"""Generate the movieplay debug-overlay font as Genesis 4bpp tiles.

Output: boot/dbgfont.bin = len(ORDER) tiles * 32 bytes.
Set pixels use colour index 1, background 0 (transparent). The player uploads
these to a reserved VRAM tile region (the top of the pool, freed by lowering
VRAM_TILES) and writes name-table entries in the bottom letterbox to print
per-frame decode metrics: F(rame) RAW CMP BUF PAL MIS.

Glyph i lives at font tile i. ORDER (index -> char) MUST match the player's
lookup table:
  0..15 = hex 0-F, 16=R 17=W 18=M 19=P 20=U 21=L 22=I 23=S 24=' ' 25='+' 26='-'
  27=N
"""
from pathlib import Path

_HEX = {
    0x0: ["..####..", ".##..##.", ".##..##.", ".##..##.", ".##..##.", ".##..##.", "..####..", "........"],
    0x1: ["...##...", "..###...", "...##...", "...##...", "...##...", "...##...", ".######.", "........"],
    0x2: [".#####..", "##...##.", "....##..", "...##...", "..##....", ".##.....", "#######.", "........"],
    0x3: ["#####...", "....##..", "...###..", "....##..", "....##..", "##..##..", ".####...", "........"],
    0x4: ["...###..", "..####..", ".##.##..", "##..##..", "#######.", "....##..", "....##..", "........"],
    0x5: ["#######.", "##......", "######..", ".....##.", ".....##.", "##...##.", ".#####..", "........"],
    0x6: ["..####..", ".##.....", "##......", "######..", "##...##.", "##...##.", ".#####..", "........"],
    0x7: ["#######.", "....##..", "...##...", "..##....", "..##....", "..##....", "..##....", "........"],
    0x8: [".#####..", "##...##.", "##...##.", ".#####..", "##...##.", "##...##.", ".#####..", "........"],
    0x9: [".#####..", "##...##.", "##...##.", ".######.", ".....##.", "....##..", ".####...", "........"],
    0xA: ["..###...", ".##.##..", "##...##.", "##...##.", "#######.", "##...##.", "##...##.", "........"],
    0xB: ["######..", "##...##.", "##...##.", "######..", "##...##.", "##...##.", "######..", "........"],
    0xC: ["..####..", ".##..##.", "##......", "##......", "##......", ".##..##.", "..####..", "........"],
    0xD: ["#####...", "##..##..", "##...##.", "##...##.", "##...##.", "##..##..", "#####...", "........"],
    0xE: ["#######.", "##......", "##......", "#####...", "##......", "##......", "#######.", "........"],
    0xF: ["#######.", "##......", "##......", "#####...", "##......", "##......", "##......", "........"],
}
_LET = {
    "R": ["######..", "##...##.", "##...##.", "######..", "##.##...", "##..##..", "##...##.", "........"],
    "W": ["##...##.", "##...##.", "##...##.", "##.#.##.", "#######.", "###.###.", "##...##.", "........"],
    "M": ["##...##.", "###.###.", "#######.", "##.#.##.", "##...##.", "##...##.", "##...##.", "........"],
    "P": ["######..", "##...##.", "##...##.", "######..", "##......", "##......", "##......", "........"],
    "U": ["##...##.", "##...##.", "##...##.", "##...##.", "##...##.", "##...##.", ".#####..", "........"],
    "L": ["##......", "##......", "##......", "##......", "##......", "##......", "#######.", "........"],
    "I": [".#####..", "...##...", "...##...", "...##...", "...##...", "...##...", ".#####..", "........"],
    "S": [".#####..", "##......", "##......", ".#####..", ".....##.", ".....##.", "######..", "........"],
    " ": ["........", "........", "........", "........", "........", "........", "........", "........"],
    "+": ["........", "...#....", "...#....", ".#####..", "...#....", "...#....", "........", "........"],
    "-": ["........", "........", "........", ".#####..", "........", "........", "........", "........"],
    "N": ["##...##.", "###..##.", "####.##.", "##.####.", "##..###.", "##...##.", "##...##.", "........"],
}
ORDER = [_HEX[v] for v in range(16)] + [_LET[c] for c in ["R", "W", "M", "P", "U", "L", "I", "S", " ", "+", "-", "N"]]


def tile_bytes(rows):
    out = bytearray()
    for row in rows:
        px = [1 if c == "#" else 0 for c in row.ljust(8, ".")[:8]]
        for i in range(0, 8, 2):
            out.append((px[i] << 4) | px[i + 1])
    return bytes(out)


def main():
    data = bytearray()
    for rows in ORDER:
        data += tile_bytes(rows)
    Path("boot/dbgfont.bin").write_bytes(bytes(data))
    print(f"wrote boot/dbgfont.bin = {len(ORDER)} tiles * 32 = {len(data)} bytes")


if __name__ == "__main__":
    main()
